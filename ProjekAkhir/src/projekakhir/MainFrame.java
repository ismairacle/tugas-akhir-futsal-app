    private void btnInputPesananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInputPesananActionPerformed

        String kodeMember = kodeMemberField.getText();
        String namaTeam = namaTeamField.getText();
        int jam1Box = jamMulaiBox.getSelectedIndex() + 8;
        int jam2Box = jamSelesaiBox.getSelectedIndex() + 9;

        Timestamp ts = utils.getTimestamp(tanggalMain, jam1Box);
        Timestamp ts2 = utils.getTimestamp(tanggalMain, jam2Box);

        int dp = Integer.valueOf(dpField.getText());

        pesanan = new Pesanan(kodeMember, namaTeam, ts, ts2, dp);

        pdo.insertPesanan(pesanan);
        int sisaBayar = pdo.cariSisaBayar(pesanan.getKodePesanan(), pesanan.getKodeMember());
        pdo.insertSisaBayar(sisaBayar, pesanan.getKodePesanan());

        JOptionPane.showMessageDialog(this, "Berhasil memesan dengan kode pesan: " + pesanan.getKodePesanan());

        kodeMemberField.setText("");
        namaTeamField.setText("");
        dpField.setText("");

    }//GEN-LAST:event_btnInputPesananActionPerformed

    private void btnKonfirmasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKonfirmasiActionPerformed

        String kodePesanan = kodePesananField.getText();
        pesanan = pdo.getPesananByKodePesanan(kodePesanan);
        pdo.setStatus(kodePesanan, Constants.BELUM_LUNAS);
        int sisaBayar = pesanan.getSisaBayar();
        if (sisaBayar == 0) {
            sisaBayarField.setText("");
            dp2Field.setText("");
            totalBayar2Field.setText("");
        } else {
            sisaBayarField.setText(String.valueOf(formatRupiah.format(sisaBayar)));
            dp2Field.setText(String.valueOf(formatRupiah.format(pesanan.getDp())));
            totalBayar2Field.setText(String.valueOf(formatRupiah.format(pesanan.getDp() + sisaBayar)));
            jPanel4.setVisible(true);
        }
        kodePesananField.setText("");
        JOptionPane.showMessageDialog(this, "Pesanan berhasil dikonfirmasi");

    }//GEN-LAST:event_btnKonfirmasiActionPerformed

