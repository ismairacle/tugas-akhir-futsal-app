    private void tampilkanPesanan(List<Pesanan> pesananList) {

        modelTablePesanan.setRowCount(0);
        SimpleDateFormat sdf = new SimpleDateFormat("dd MMM");
        int no = 1;
        for (Pesanan pesanan1 : pesananList) {

            Date tanggal = utils.timeStampToDate(pesanan1.getJamMulai());
            String lamaMain = String.valueOf(utils.getLamaMain(pesanan1)) + " Jam";
            int totalBayar = pesanan1.getDp() + pesanan1.getSisaBayar();
            modelTablePesanan.addRow(new Object[]{no,
                pesanan1.getKodePesanan(),
                sdf.format(tanggal),
                pesanan1.getNamaTeam(),
                lamaMain,
                pesanan1.getStatus(),
                formatRupiah.format(totalBayar)
            });
            no++;
        }

        //menampilkan total penghasilan
        int totalPendapatan = pdo.getTotalPendapatan();
        pendapatanField.setText(formatRupiah.format(totalPendapatan));

        //menampilkan total lama sewa
        int totalLamaSewa = pdo.getTotalLamaSewa();
        lamaSewaField.setText(String.valueOf(totalLamaSewa) + " Jam");

    }

    private void tampilkanMember(List<Member> memberList) {

        modelTableMember.setRowCount(0);
        int no = 1;
        for (Member member1 : memberList) {
            modelTableMember.addRow(new Object[]{
                no,
                member1.getIdMember(),
                member1.getNamaMember(),
                member1.getAlamat(),
                member1.getKontak(),
                no++

            });
        }

    }

    private void showAllMember() {
        List<Member> member = mdo.getAllMember();
        tampilkanMember(member);

    }

