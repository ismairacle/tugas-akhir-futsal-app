    private void btnLunasiPembayaranActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLunasiPembayaranActionPerformed
        String kodePesanan = pesanan.getKodePesanan();
        int sisaBayar = pesanan.getSisaBayar();
        int sisaBayarInput = Integer.parseInt(sisaBayarField1.getText());

        if (sisaBayarInput >= sisaBayar) {
            pdo.setStatus(kodePesanan, Constants.SELESAI);
            JOptionPane.showMessageDialog(this, "Pesanan " + kodePesanan + " selesai.");
            sisaBayarField.setText("");
            dp2Field.setText("");
            totalBayar2Field.setText("");
            if (sisaBayarInput > sisaBayar) {
                int kembalian = sisaBayarInput - sisaBayar;
                JOptionPane.showMessageDialog(this, "Kembalian : " + kembalian);
            }
        } else if (sisaBayarInput < sisaBayar) {
            JOptionPane.showMessageDialog(this, "uang kurang: ");
        }

        sisaBayarField1.setText("");


    }//GEN-LAST:event_btnLunasiPembayaranActionPerformed

